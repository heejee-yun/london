package com.augfw.infra.modules.codegroup;

public class CodeGroup {

//	나한테 있는 코드 그룹 컬럼

//	code_group
	private String sep;
	private Integer oderny;
	private String name;
	private Integer userny;
	
	
//-------------	
	public String getSep() {
		return sep;
	}
	public void setSep(String sep) {
		this.sep = sep;
	}
	public Integer getOderny() {
		return oderny;
	}
	public void setOderny(Integer oderny) {
		this.oderny = oderny;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getUserny() {
		return userny;
	}
	public void setUserny(Integer userny) {
		this.userny = userny;
	}

	
}
